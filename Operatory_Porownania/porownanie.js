
// Operatory porównania

var x = 5 == 5; // operator porównania "=="
console.log(x);
console.log(typeof(x));

console.log(5 == "5"); // sprawdza tylko wartości
console.log(5 === "5"); // sprawdza wartości i typy danych

console.log(5 != 5); // operator 'różny' - czy 5 jest 'różne' od 5
console.log(5 !== "5") // operator 'różny' który porównóje też  typy danych

console.log(5 > 5); // operator większości
console.log(5 <= 5); // operator większy-równy

