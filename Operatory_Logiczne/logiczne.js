
// Operatory Logiczne

console.log(5 >= 5 || 5 == 1); // operator 'LUB' || - ten operator zwraca wartość prawdy jeżeli przynajmniej po jednej ze stron będzie otrzyma wartość 'true'

console.log(true && false); // operator 'i' &&, - tylko w przypadku dwóch prawd (true) zwraca wrtość 'true'

console.log(! 5 > 0); // operator negacje '!' - odwraca wartość wyrażenia

console.log(true || true && false);

// operatow  'i' && jest ważniejszy od 'lub' ||

console.log((true || true) && ! false);